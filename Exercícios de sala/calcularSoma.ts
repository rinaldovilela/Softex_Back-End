function somar(num:number, num2:number): number {
    return num + num2
}

console.log(somar(2,1))


function subtrair(num:number, num2:number): number {
    return num - num2;
}

console.log(subtrair(2,1))


function multiplicar(num:number, num2:number): number {
    return num * num2
}

console.log(multiplicar(2,9))


function dividir(num:number, num2:number): number {
    return num / num2
}

console.log(dividir(10,2))


function juntarNomes(nome1: string, nome2: string): string {

return nome1 + " " + nome2
}

console.log(juntarNomes("Harry", "Potter"))